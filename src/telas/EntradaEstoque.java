    private void txtCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCodigoActionPerformed
        // ENTER NO CÓDIGO
        try {
            Produto p = new Produto();
            
            p.setCodProduto(txtCodigo.getText());   
            p.setNomeProduto(null);
            
            ProdutoDAO pdao = new ProdutoDAO();
            pdao.preencher(p);
            
            if (p.getNomeProduto() != null){
                txtNome.setText(p.getNomeProduto());
                txtValorCusto.setText("R$ " + String.valueOf(p.getPrecoCusto()).format("%.2f", p.getPrecoCusto()));
                txtValorVenda.setText("R$ " + String.valueOf(p.getPrecoVenda()).format("%.2f", p.getPrecoVenda()));

                txtQuantidade.requestFocus();
            }
            else{
                JOptionPane.showMessageDialog(null, "Produto não cadastrado no estoque");
                txtValorCusto.setText("");
                txtValorVenda.setText("");
                txtCodigo.setText("");
                txtCodigo.requestFocus();
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao preencher campos: " + ex.getMessage());
        }    
    }//GEN-LAST:event_txtCodigoActionPerformed

