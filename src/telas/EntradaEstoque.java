    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // CONFIRMAR
        
        if (txtCodigo.getText().trim().equals("") || txtQuantidade.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!");
            return;
        }
        
        Produto p = new Produto();     
        p.setCodProduto(txtCodigo.getText());       
        p.setNomeProduto(null);
        ProdutoDAO pdao = new ProdutoDAO();
        
        try {
            pdao.preencher(p);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro inesperado: " + ex.getMessage());
        }
        
        if (p.getNomeProduto() != null){
        try {
            
            p.setQtdEstoque(parseInt(txtQuantidade.getText()));
            p.setCodProduto(txtCodigo.getText());   
            
            pdao.entrada(p);
            txtCodigo.requestFocus();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao dar entrada no estoque: " + ex.getMessage());
            System.out.println(p.getQtdEstoque());
        }
        txtCodigo.setText("");
        txtNome.setText("");
        txtValorCusto.setText("");
        txtValorVenda.setText("");
        txtQuantidade.setText("");
        txtCodigo.requestFocus();
        }
        else{
           JOptionPane.showMessageDialog(null, "Produto não cadastrado no estoque"); 
           txtValorCusto.setText("");
           txtValorVenda.setText("");
           txtQuantidade.setText("");
           txtCodigo.setText("");
           txtCodigo.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

